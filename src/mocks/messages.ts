// ─── Relative date helpers ────────────────────
// Ensures mock data always has "today" messages so the Today divider works.

function today(hours: number, minutes = 0): Date {
  const d = new Date();
  d.setHours(hours, minutes, 0, 0);
  return d;
}

function daysAgo(days: number, hours: number, minutes = 0): Date {
  const d = new Date();
  d.setDate(d.getDate() - days);
  d.setHours(hours, minutes, 0, 0);
  return d;
}

